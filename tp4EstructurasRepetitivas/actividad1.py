"""Crea un programa que imprima en pantalla todos los números enteros desde 0 hasta 100
(incluyendo ambos extremos), en orden creciente, mostrando un número por línea."""
# Se utilizan variables para definir el rango de números a imprimir con el fin de que el código sea más legible y fácil de modificar.
minimo = 0
maximo = 100
for i in range(minimo, maximo + 1):
    print(i)

